package Charges;


/**
 *
 * @author instm
 */
public class Charges {
     private int idCharges;
   private int loyer;
   private int electricite;
   private int gaz;
   private int wifi;

    public int getIdCharges() {
        return idCharges;
    }

    public void setIdCharges(int idCharges) {
        this.idCharges = idCharges;
    }

    public int getLoyer() {
        return loyer;
    }

    public void setLoyer(int loyer) {
        this.loyer = loyer;
    }

    public int getElectricite() {
        return electricite;
    }

    public void setElectricite(int electricite) {
        this.electricite = electricite;
    }

    public int getGaz() {
        return gaz;
    }

    public void setGaz(int gaz) {
        this.gaz = gaz;
    }

    public int getWifi() {
        return wifi;
    }

    public void setWifi(int wifi) {
        this.wifi = wifi;
    }
   
   
}
